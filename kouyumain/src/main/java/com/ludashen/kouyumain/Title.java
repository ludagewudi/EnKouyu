public class Title {
    private int id;
    private String name;
    private Topic topicid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Topic getTopicid() {
        return topicid;
    }

    public void setTopicid(Topic topicid) {
        this.topicid = topicid;
    }
}
